    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String sql = "update appusers set name=?, username=?,  password=?, role=?, email=?, contact=? where empid=?";
        
        try {
            pst = dbConn.getCon().prepareStatement(sql);
            
            pst.setString(1, dbName.getText().trim());
            pst.setString(2, dbUsername.getText().trim());
            pst.setString(3, dbPassword.getText().trim());
            pst.setString(4, dbRole.getItemAt(0));
            pst.setString(5, dbEmail.getText().trim());
            pst.setString(6, dbContact.getText().trim());
            pst.setString(7, dbEmpid.getText());

             pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "User Data Updated!");
            showData();
            clear();
            pst.close();
            dbConn.getCon().close();
            
            
            
        } catch (ClassNotFoundException ex) {
             JOptionPane.showMessageDialog(this, "User Data Updated! Failed!");
            Logger.getLogger(AppUserManager.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AppUserManager.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

