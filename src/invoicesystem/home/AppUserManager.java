    public void showData() throws SQLException{
    
        String sql="select * from appusers";
        
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(columns);
        userTable.setModel(model);
        ResultSet rs;
        
        try {
            pst = dbConn.getCon().prepareStatement(sql);
            rs = pst.executeQuery();
            
            while(rs.next()){
                String empid = rs.getString("empid");
                String name = rs.getString("name");
                String username = rs.getString("username");
                String password = rs.getString("password");
                String role = rs.getString("role");
                String email = rs.getString("email");
                String contact = rs.getString("contact");
                
                model.addRow(new Object[]{empid, name, username, password, role, email, contact });
                
        
        }
            
            pst.close();
            dbConn.getCon().close();
            
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AppUserManager.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    }
    
