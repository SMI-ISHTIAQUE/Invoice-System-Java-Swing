    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            String sql = "select productid, product_name, product_details, product_price, stock_status from newproduct where productid=?";
            ResultSet rs;
            
            pst=dbConn.getCon().prepareStatement(sql);
            pst.setString(1, txtSearch.getText().trim());
            
            rs = pst.executeQuery();
            
             if(rs.next()){
            txtDelproid.setText(rs.getString(1));
            txtDelname.setText(rs.getString(2));
            txtDeldetails.setText(rs.getString(3));
            txtDelprice.setText(rs.getString(4));
            txtDelstat.setText(rs.getString(5));
            
            }else{
             
             JOptionPane.showMessageDialog(null, "Product Not Found!");
             }

        } catch (ClassNotFoundException ex) {
            
            Logger.getLogger(RemoveProducts.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Product Not Found!");
        } catch (SQLException ex) {
            Logger.getLogger(RemoveProducts.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_btnSearchActionPerformed

