    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        

            
            
        String sId = txtSearch.getText().trim();
        int msg = JOptionPane.showConfirmDialog(this, "Do You Want to Delete!", "Select", JOptionPane.YES_NO_OPTION);
        
        if (msg ==0) {
            
            try {
            String sql = "delete from newproduct where productid='"+sId+"'";

                
            pst=dbConn.getCon().prepareStatement(sql);
            pst.executeUpdate();
            
            dbConn.getCon().close();
            pst.close();
            
            JOptionPane.showMessageDialog(this, "Data Deleted Successful!");
            
            setVisible(false);
            new RemoveProducts().setVisible(true);
            dispose();
                
            } catch (Exception e) {
                 JOptionPane.showMessageDialog(this, "Data Deletion Failed!");
            }

        }


        
       
    }//GEN-LAST:event_btnDeleteActionPerformed

