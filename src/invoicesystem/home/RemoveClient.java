    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        

            
            
        String csID = txtSearch.getText().trim();
        int msg = JOptionPane.showConfirmDialog(this, "Do You Want to Delete!", "Select", JOptionPane.YES_NO_OPTION);
        
        if (msg ==0) {
            
            try {
            String sql = "delete from newuser where csid='"+csID+"'";

                
            pst=dbConn.getCon().prepareStatement(sql);
            pst.executeUpdate();
            
            dbConn.getCon().close();
            pst.close();
            
            JOptionPane.showMessageDialog(this, "Client Deleted Successful!");
            
            setVisible(false);
            new RemoveClient().setVisible(true);
            dispose();
                
            } catch (Exception e) {
                 JOptionPane.showMessageDialog(this, "Client Deletion Failed!");
            }

        }


        
       
    }//GEN-LAST:event_btnDeleteActionPerformed

