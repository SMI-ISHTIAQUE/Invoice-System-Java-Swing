    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            String sql = "select csid, name, email, contact, address from newuser where csid=?";
            ResultSet rs;
            
            pst=dbConn.getCon().prepareStatement(sql);
            pst.setString(1, txtSearch.getText().trim());
            
            rs = pst.executeQuery();
            
             if(rs.next()){
            txtDelcsid.setText(rs.getString(1));    
            txtDelName.setText(rs.getString(2));
            txtDelEmail.setText(rs.getString(3));
            txtDelPhone.setText(rs.getString(4));
            txtDelAddress.setText(rs.getString(5));
            
            }else{
             
             JOptionPane.showMessageDialog(null, "Client Not Found!");
             }

        } catch (ClassNotFoundException ex) {
            
            Logger.getLogger(RemoveClient.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(RemoveClient.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_btnSearchActionPerformed

