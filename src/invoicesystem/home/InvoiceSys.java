    private void btnProceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProceedActionPerformed
        // TODO add your handling code here:
        
         DateTimeFormatter dateCreate= DateTimeFormatter.ofPattern("dd-MM-yyyy");
        LocalDateTime now = LocalDateTime.now();
        String date = dateCreate.format(now);
        
        DateTimeFormatter timeCreate= DateTimeFormatter.ofPattern("hh:mm:ss a");
        LocalDateTime tm = LocalDateTime.now();
        String time = timeCreate.format(tm);
        
        String sql = "insert into transactions(invoiceid, csid, name, contact, address, productname, productqty, vatamount, total, date_created, time_created, created_by) values(?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?,?)";
        
            try {
                pst=dbConn.getCon().prepareStatement(sql);
                
            pst.setString(1, txtinvoiceid.getText().toString().trim());
            pst.setString(2, txtCsid.getText().trim());
            pst.setString(3, txtNewname.getText().trim());
            pst.setString(4, txtNewcontact.getText().trim());
            pst.setString(5, txtNewaddress.getText().trim());
            pst.setString(6, txtProname.getText().trim());
            pst.setString(7, txtProQuantity.getText().trim());
            pst.setString(8, txtVAT.getText().trim());
            pst.setString(9, txtBal.getText().trim());
            pst.setString(10, time);
            pst.setString(11, date);
            pst.setString(12, txtUserlog.getText().trim());
                
             pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data Saved Successful!");
            pst.close();
            dbConn.getCon().close();
             
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(InvoiceSys.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(InvoiceSys.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        
        
        
        
        
    }//GEN-LAST:event_btnProceedActionPerformed

