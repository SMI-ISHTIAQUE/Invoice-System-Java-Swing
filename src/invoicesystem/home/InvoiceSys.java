    private void txtProidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtProidActionPerformed
        // TODO add your handling code here:
        String productid= txtProid.getText().trim();
        String sql = "select * from newproduct where productid like '"+productid+"%'";
        
            try {
                pst=dbConn.getCon().prepareStatement(sql);
                ResultSet rs;
                rs = pst.executeQuery(); 
                
                if(rs.next()){
                    
                 txtProid.setText(rs.getString(1));
                 txtProname.setText(rs.getString(2));
                 txtProdetails.setText(rs.getString(3));
                 txtProstat.setText(rs.getString(5));
                 txtProprice.setText(rs.getString(4));
                 txtProQuantity.setText("1");
                
                }else{
                
                 txtProname.setText("");
                 txtProdetails.setText("");
                 txtProstat.setText("");
                 txtProprice.setText("");
                 txtProQuantity.setText("");
                }
                
            } catch (ClassNotFoundException ex) {
                
                JOptionPane.showMessageDialog(this, "Failed!");
                Logger.getLogger(InvoiceSys.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(InvoiceSys.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_txtProidActionPerformed

