    private void btnUpdateproductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateproductActionPerformed
                    // TODO add your handling code here:
        String sql = "update newproduct set product_name=?, product_details=?, product_price=?, stock_status=? where productid=?";

            
         try {   
            pst = dbConn.getCon().prepareStatement(sql);
            

            pst.setString(1, txtProname.getText().trim());
            pst.setString(2, txtProndetails.getText().trim());
            pst.setString(3, txtProprice.getText().trim());
            pst.setString(4, txtProstat.getSelectedItem().toString().trim());
            pst.setInt(5, Integer.parseInt(txtProid.getText().trim()));
            
            
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Product Update Successful");
            
             pst.close();
            dbConn.getCon().close();
            
             setVisible(false);
             new UpdateProducts().setVisible(true);
            

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Operation Failed!");
            Logger.getLogger(UpdateProducts.class.getName()).log(Level.SEVERE, null, ex);
        } 
         catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Operation Failed!");
            Logger.getLogger(UpdateProducts.class.getName()).log(Level.SEVERE, null, ex);
        }
        
           
        
    }//GEN-LAST:event_btnUpdateproductActionPerformed

