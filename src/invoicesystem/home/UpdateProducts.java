    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        try {
            // TODO add your handling code here:
            String sql = "select productid, product_name, product_details, product_price, stock_status from newproduct where productid=?";
            ResultSet rs;
            pst=dbConn.getCon().prepareStatement(sql);
            pst.setString(1, txtProsearch.getText().trim());
            
            rs = pst.executeQuery();
            
            while(rs.next()){
            
            txtProid.setText(rs.getString(1));
            txtProname.setText(rs.getString(2));
            txtProndetails.setText(rs.getString(3));
            txtProprice.setText(rs.getString(4));
            txtProstat.setSelectedItem(rs.getString(5));

            
  
            
            }
 
            
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Product ID Not Found!");
            Logger.getLogger(UpdateProducts.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Product ID Not Found!");
            Logger.getLogger(UpdateProducts.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

